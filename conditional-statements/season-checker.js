const monthNumber = 8;
if(monthNumber >= 9 && monthNumber <= 12){
    console.log("Winter");
}
else if(monthNumber >= 5 && monthNumber <= 8){
    console.log("Monsoon");
}
else if(monthNumber >= 1 && monthNumber <= 4){
    console.log("Summer");
}
else{
    console.log("This month number is not valid");
}