console.log('for loop print: ');
for (let f = 1; f <= 5; f++) {
    console.log(f);
}

console.log();

console.log('while loop print: ');
let w = 1;
while (w <= 5) {
    console.log(w);
    w++;
}

console.log();

console.log('do-while loop print: ');
let d = 1;
do {
    console.log(d);
    d++;
} while (d <= 5);